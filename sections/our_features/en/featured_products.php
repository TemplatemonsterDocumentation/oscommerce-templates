<h3>Featured Products</h3>
<p>This feature allows to select store products that will show up on the website as recommended products.</p>
<p>You can navigate to Catalog>Featured Products to add featured products:</p>
<figure class="img-polaroid">
    <img src="img/featured-products-1.jpg" alt="Featured Products"/>
</figure>
<p>You can see the list of Featured Products there.</p>
<p>In <strong>"Status"</strong> field you can show/hide the product in Featured Products section (it will stay in the
    list of featured products but it will not show up on the site).</p>
<p>In <strong>"Actions"</strong> field you can check, change or delete product details.</p>
<p>To modify the product details, you can click <strong>"Edit"</strong> button:</p>
<p>After filling in the required parameters, you can click <strong>"Update"</strong> button.</p>
<figure class="img-polaroid">
    <img src="img/featured-products-2.jpg" alt="Featured Products"/>
</figure>
<p>To add a new product to the Featured Products list, click <strong>"New product"</strong> button:</p>
<p>In the drop down list select the product you want to add and set the expiry date when it should be removed from the
    Featured products section, then click <strong>"Insert"</strong> button.</p>
<figure class="img-polaroid">
    <img src="img/featured-products-3.jpg" alt="Featured Products"/>
</figure>
<p>You can also use "Search" button:</p>
<p>You can search by product name and model.</p>
<figure class="img-polaroid">
    <img src="img/featured-products-4.jpg" alt="Featured Products"/>
</figure>
<p>These products can be displayed in the sidebar block:</p>
<figure class="img-polaroid">
    <img src="img/featured-products-5.jpg" alt="Featured Products"/>
</figure>
<p>On home page or category page that has subcategories:</p>
<figure class="img-polaroid">
    <img src="img/featured-products-6.jpg" alt="Featured Products"/>
</figure>
