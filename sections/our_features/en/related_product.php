<h3>Related Products</h3>
<p>This feature allows to add relations between products.</p>
<p>You can add relations under <strong>Catalog>Related Products</strong> section:</p>
<p>You will see the list of related products (if they exist) and tools to manage them.</p>
<p>The drop down list at the top will help you to filter relations for specific product or for all products, by default
    it is set to <strong>Show All Products</strong>.</p>
<p>Under the drop down list there is a pages navigation (required if the list has more than 10 items).</p>
<h6>The list below has the following fields:</h6>
<ol class="index-list">
    <li>Product to which another product relates.</li>
    <li>Product that relates.</li>
    <li>Sort order.</li>
    <li>Edit and Delete buttons.</li>
</ol>
<h6>You can add new relations using the drop down lists below. You should fill in the following fields:</h6>
<ol class="index-list">
    <li>Product to which another product relates.</li>
    <li>Product that relates.</li>
    <li>Sort order.</li>
</ol>
<h6>After adding relations use the following buttons to save the changes:</h6>
<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Insert</dt>
            <dd>will add new relation to the list.</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Reciprocate</dt>
            <dd>will add new relation to the list (for both products).</dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Inherit</dt>
            <dd>will add new relation to the list (only for one product).</dd>
        </dl>
    </li>
</ul>
