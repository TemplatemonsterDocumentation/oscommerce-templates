<h3>Tags</h3>
<p>This feature allows to add tags to the products and search products by these tags.</p>
<p>You can navigate to <strong>Catalog>Tags</strong> in order to add tags:</p>
<p>You will see the list of all products there.</p>
<figure class="img-polaroid">
    <img src="img/tags-products-1.jpg" alt="Tags Products"/>
</figure>
<p>To add or modify tags, select the required product and click <strong>"Edit"</strong> button.</p>
<figure class="img-polaroid">
    <img src="img/tags-products-2.jpg" alt="Tags Products"/>
</figure>
<p>In Tags field, add tags separated with commas and click <strong>"Save"</strong>.</p>
<p>If you want to remove all tags from the product, you can click <strong>"Delete"</strong> button.</p>
<p>In your store tags will show up in the sidebar block:</p>
<figure class="img-polaroid">
    <img src="img/tags-products-3.jpg" alt="Tags Products"/>
</figure>
<p>In the block on product page:</p>
<figure class="img-polaroid">
    <img src="img/tags-products-4.jpg" alt="Tags Products"/>
</figure>
<p>If you click the tag, you will see the list of products that have it:</p>
<figure class="img-polaroid">
    <img src="img/tags-products-5.jpg" alt="Tags Products"/>
</figure>