<h3>Nivo Slider</h3>
<p>This feature allows to add frontend banners with captions.</p>
<p>You can navigate to <strong>Configuration > Nivo Slider</strong> to configure the slider:</p>
<p>You can add all the required slider effects and select the group of banners that will show up in your slider.</p>
<figure class="img-polaroid">
    <img src="img/nivo-1.jpg" alt="Nivo"/>
</figure>
<p class="alert alert-warning"> We will describe how to add banners in the section Creating
        Banners.</p>
<p>To display the slider on your frontend, you can navigate to <strong>Modules > Boxes > Nivo Slider</strong> and
    select one of two positions (Under Header Block/Home Content Block) for your slider and its sort order.</p>