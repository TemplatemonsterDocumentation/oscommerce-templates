<h3>Modules > Boxes</h3>
<p>16 modules were added to the template to extend functionality of the website. They are located under <strong>Modules
        > Boxes</strong>.</p>
<p>To set the module, select the module and click <strong>"Edit"</strong> button:</p>
<p>You will see the screen where you can add all the block settings (Module Header Hopping Cart is used as an
    example).</p>
<p>For example:</p>
<ol class="index-list">
    <li>Enable or disable on the frontend.</li>
    <li>Module position (positions in other modules may differ from positions described in the example) Header
        Top/Header.
    </li>
    <li>Sort order in the position where the module will show up.</li>
    <li>Enable/disable the hopping cart (only for the described module, other modules will have other options).</li>
    <li>Pages where the module will show up.</li>
</ol>
<p>Click <strong>"Save"</strong> button to save the settings.</p>
