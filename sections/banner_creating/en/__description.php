<h2 class="item3">Creating Banners</h2>
<p>To add or to modify a banner or a group of banners, navigate to <strong>Tools > Banner Manager</strong>:</p>
<p>To modify the banner, click <strong>"Edit"</strong>:</p>
<figure class="img-polaroid">
    <img src="img/banners-1.jpg" alt="banners"/>
</figure>
<p>Fill in the required fields, add image and/or description, select the existing group of banners or create the new
    one, then click <strong>"Save"</strong>.</p>
<figure class="img-polaroid">
    <img src="img/banners-2.jpg" alt="banners"/>
</figure>
<p>To add a new banner, click <strong>"New banner"</strong> button and do the same steps as described for editing
    banners.</p>
<p class="alert alert-info">You can use image, text or both to create the banner.</p>
<p>Groups of banners can be displayed on the website with the help of <a href="#nivo_slider">Nivo Slider</a>, <a
        href="#nivo_slider">Banner Box</a> or <a href="#banners_blocks">Content Banner Box</a>.</p>